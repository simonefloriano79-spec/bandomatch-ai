def popola_database():
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()
    
    ora = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    inseriti = 0
    aggiornati = 0
    
    for bando in BANDI:
        # Verifica se esiste già
        c.execute("SELECT id FROM bandi WHERE nome = ?", (bando["nome"],))
        esistente = c.fetchone()
        
        if esistente:
            # Aggiorna
            c.execute("""
                UPDATE bandi SET 
                    ente=?, regione=?, tipo=?, stato=?, data_apertura=?, data_scadenza=?,
                    massimale=?, percentuale_fondo_perduto=?, ateco_ammessi=?, ateco_esclusi=?,
                    eta_min_impresa_mesi=?, eta_max_impresa_mesi=?, eta_min_soci=?, eta_max_soci=?,
                    forma_giuridica_ammessa=?, regioni_ammesse=?, requisiti_extra=?, url=?,
                    data_aggiornamento=?, fonte_scraping=?, tasso_successo=?
                WHERE nome = ?
            """, (
                bando["ente"], bando["regione"], bando["tipo"], bando["stato"],
                bando["data_apertura"], bando["data_scadenza"], bando["massimale"],
                bando["percentuale_fondo_perduto"], bando["ateco_ammessi"], bando["ateco_esclusi"],
                bando["eta_min_impresa_mesi"], bando["eta_max_impresa_mesi"],
                bando["eta_min_soci"], bando["eta_max_soci"],
                bando["forma_giuridica_ammessa"], bando["regioni_ammesse"],
                bando["requisiti_extra"], bando["url"], ora,
                bando["fonte_scraping"], bando["tasso_successo"],
                bando["nome"]
            ))
            aggiornati += 1
        else:
            # Inserisci
            c.execute("""
                INSERT INTO bandi (
                    nome, ente, regione, tipo, stato, data_apertura, data_scadenza,
                    massimale, percentuale_fondo_perduto, ateco_ammessi, ateco_esclusi,
                    eta_min_impresa_mesi, eta_max_impresa_mesi, eta_min_soci, eta_max_soci,
                    forma_giuridica_ammessa, regioni_ammesse, requisiti_extra, url,
                    data_aggiornamento, fonte_scraping, tasso_successo
                ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
            """, (
                bando["nome"], bando["ente"], bando["regione"], bando["tipo"], bando["stato"],
                bando["data_apertura"], bando["data_scadenza"], bando["massimale"],
                bando["percentuale_fondo_perduto"], bando["ateco_ammessi"], bando["ateco_esclusi"],
                bando["eta_min_impresa_mesi"], bando["eta_max_impresa_mesi"],
                bando["eta_min_soci"], bando["eta_max_soci"],
                bando["forma_giuridica_ammessa"], bando["regioni_ammesse"],
                bando["requisiti_extra"], bando["url"], ora,
                bando["fonte_scraping"], bando["tasso_successo"]
            ))
            inseriti += 1
    
    conn.commit()
    
    # Verifica finale
    c.execute("SELECT COUNT(*) FROM bandi")
    totale = c.fetchone()[0]
    
    c.execute("SELECT COUNT(*) FROM bandi WHERE stato='aperto'")
    aperti = c.fetchone()[0]
    
    c.execute("SELECT regioni_ammesse, COUNT(*) FROM bandi GROUP BY regioni_ammesse ORDER BY COUNT(*) DESC LIMIT 10")
    per_regione = c.fetchall()
    
    conn.close()
    
    print(f"✅ Database popolato con successo!")
    print(f"   Inseriti: {inseriti} nuovi bandi")
    print(f"   Aggiornati: {aggiornati} bandi esistenti")
    print(f"   Totale bandi nel DB: {totale}")
    print(f"   Bandi aperti: {aperti}")
    print(f"\n📊 Distribuzione per regione:")
    for regione, count in per_regione:
        print(f"   {regione}: {count} bandi")


if __name__ == "__main__":
    popola_database()
